async function main() {
  await sequelize.authenticate();

  let created = 0;
  let existing = 0;
  const missing = [];

  for (const [processName, code, title] of records) {
    const scheduleId = scheduleByProcess.get(processName);
    if (!scheduleId) {
      missing.push({ processName, code });
      continue;
    }

    const scheduleItem = await ScheduleItem.findOne({ where: { id: scheduleId, proyecto_id: projectId } });
    if (!scheduleItem) {
      missing.push({ processName, code, scheduleId });
      continue;
    }

    const [task, wasCreated] = await Task.findOrCreate({
      where: { proyecto_id: projectId, titulo: `${code} - ${title}` },
      defaults: {
        proyecto_id: projectId,
        cronograma_item_id: scheduleId,
        titulo: `${code} - ${title}`,
        descripcion: `Proceso: ${processName}`,
        tipo: 'DESARROLLO',
        prioridad: 'MEDIA',
        estado: 'BACKLOG',
        avance: 0,
        bloqueado: false,
        defectos: 0,
      },
    });

    if (!wasCreated && Number(task.cronograma_item_id) !== Number(scheduleId)) {
      await task.update({ cronograma_item_id: scheduleId, descripcion: `Proceso: ${processName}` });
    }

    if (wasCreated) created += 1;
    else existing += 1;
  }

  console.log(JSON.stringify({ created, existing, missing, total: records.length }, null, 2));
}

main()
  .catch((error) => {
    console.error(error);
    process.exitCode = 1;
  })
  .finally(async () => {
    await sequelize.close();
  });
